/**
 * Prestige («Сжатие»): коллапс рана ради постоянной валюты PP, ускоряющей
 * следующие заходы.
 *
 * PP считается по МАССЕ, ПОТРАЧЕННОЙ за ран (`massSpentRun`), а не по той, что
 * лежит на руках: награда за реальное развитие (улучшения, планеты), а не за
 * накопительство. Формула — корневая (убывающая отдача), порог = делитель.
 * Числа — стартовая калибровка, уточняются плейтестом (фаза G).
 */

/**
 * Сколько потраченной массы соответствует «одному шагу» PP (порог первого PP).
 *
 * V2-калибровка (по офлайн-зонду темпа): при 5000 первое осмысленное сжатие
 * (~45–60 мин, потрачено ~8–12k) давало лишь 1 PP — на связку перков не хватает.
 * Снижено 5000 → 1000: то же сжатие даёт ~3 PP, а корневая формула (sqrt) не даёт
 * поздним ранам раздуться (45k потрачено → 6 PP, не десятки).
 */
export const PRESTIGE_SPENT_PER_PP = 1000;

/** PP по абсолютной массе-основе (0, если ниже порога). */
export function ppFromSpent(amount: number): number {
  if (amount < PRESTIGE_SPENT_PER_PP) return 0;
  return Math.floor(Math.sqrt(amount / PRESTIGE_SPENT_PER_PP));
}

/**
 * База PP сжатия = ПОТРАЧЕНО за ран + НАЛИЧИЕ на руках. Учитываем и то, и другое:
 * масса, которой ты управляешь сейчас, — тоже твоя (по фидбеку пользователя), а
 * не только уже вложенная в улучшения.
 */
export function prestigePpGain(massSpentRun: number, massMp: number): number {
  return ppFromSpent(Math.max(0, massSpentRun) + Math.max(0, massMp));
}
